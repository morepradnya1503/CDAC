public class MultiplesOfSeven {
    public static void main(String[] args) {
        // Loop from 1 to 100
        for (int i = 1; i <= 100; i++) {
            // Check if the current number is a multiple of 7
            if (i % 7 == 0) {
                System.out.println(i);  // Print the number
            }
        }
    }
}
